class ReadmeGenerator:
    """Generates a professional README.md from a ProjectSpec and ForgeOutput.

    Uses an inline Jinja2 template to produce consistent, structured markdown
    suitable for portfolio GitHub repositories.
    """

    def __init__(self) -> None:
        self._template: Template = Template(_README_TEMPLATE)
        self._log = structlog.get_logger(self.__class__.__name__)

    def generate(self, project_spec: ProjectSpec, forge_output: ForgeOutput) -> str:
        """Render a full README markdown string.

        Args:
            project_spec: The project specification from Layer 4.
            forge_output: The code-forge output from Layer 5.

        Returns:
            Complete README.md content as a string.
        """
        self._log.info(
            "readme_generator.generating",
            project_name=project_spec.project_name,
            quality_score=forge_output.quality_score,
        )

        # Derive computed values
        test_results: dict[str, bool] = forge_output.test_results
        tests_passed = sum(1 for v in test_results.values() if v)
        tests_total = len(test_results)
        test_summary = tests_total > 0

        mermaid_body = _build_mermaid(project_spec.file_tree, project_spec.feature_specs)
        tree_lines = _render_file_tree(project_spec.file_tree)
        file_tree_display = project_spec.project_name + "/\n" + "\n".join(tree_lines)

        repo_slug = _slugify(project_spec.project_name)
        install_cmd = _detect_install_command(project_spec.tech_stack)
        run_cmd = _detect_run_command(project_spec.tech_stack)
        test_cmd = _detect_test_command(project_spec.tech_stack)

        context: dict[str, object] = {
            "project_name": project_spec.project_name,
            "one_liner": project_spec.one_liner,
            "quality_score": forge_output.quality_score,
            "quality_badge_color": _quality_badge_color(forge_output.quality_score),
            "mermaid_diagram": mermaid_body,
            "features": project_spec.feature_specs,
            "repo_slug": repo_slug,
            "install_command": install_cmd,
            "run_command": run_cmd,
            "tech_stack": project_spec.tech_stack,
            "file_tree_display": file_tree_display,
            "adrs": project_spec.adr_list,
            "test_command": test_cmd,
            "test_summary": test_summary,
            "test_results": test_results,
            "tests_passed": tests_passed,
            "tests_total": tests_total,
        }

        readme = self._template.render(**context)

        self._log.info(
            "readme_generator.done",
            project_name=project_spec.project_name,
            readme_chars=len(readme),
        )
        return readme
