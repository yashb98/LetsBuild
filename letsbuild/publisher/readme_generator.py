# ---------------------------------------------------------------------------
# Helper utilities
# ---------------------------------------------------------------------------


def _slugify(name: str) -> str:
    """Convert a project name to a URL-safe slug."""
    return name.lower().replace(" ", "-").replace("_", "-")


def _quality_badge_color(score: float) -> str:
    """Return a shields.io colour based on quality score."""
    if score >= 90.0:
        return "brightgreen"
    if score >= 70.0:
        return "yellow"
    return "red"


def _build_mermaid(
    file_tree: list[FileTreeNode],
    feature_specs: list,  # list[FeatureSpec]
) -> str:
    """Produce a Mermaid flowchart body from feature specs (or file tree as fallback)."""
    lines: list[str] = []

    if feature_specs:
        # Use feature dependency graph when available
        node_ids: dict[str, str] = {}
        for i, feat in enumerate(feature_specs):
            node_id = f"F{i}"
            node_ids[feat.feature_name] = node_id
            lines.append(f'    {node_id}["{feat.feature_name}"]')

        for feat in feature_specs:
            src = node_ids[feat.feature_name]
            for dep_name in feat.dependencies:
                if dep_name in node_ids:
                    dep_id = node_ids[dep_name]
                    lines.append(f"    {dep_id} --> {src}")
    else:
        # Fallback: render top-level file-tree nodes as a linear chain
        top_dirs = [n for n in file_tree if n.is_directory][:6]
        for i, node in enumerate(top_dirs):
            label = node.path.strip("/")
            lines.append(f'    N{i}["{label}"]')
            if i > 0:
                lines.append(f"    N{i - 1} --> N{i}")

    return "\n".join(lines) if lines else '    A["Project"]'


def _render_file_tree(nodes: list[FileTreeNode], prefix: str = "") -> list[str]:
    """Recursively render a file tree in ASCII-art style."""
    lines: list[str] = []
    for i, node in enumerate(nodes):
        connector = "└── " if i == len(nodes) - 1 else "├── "
        name = node.path.split("/")[-1] or node.path
        suffix = "/" if node.is_directory else ""
        desc = f"  # {node.description}" if node.description else ""
        lines.append(f"{prefix}{connector}{name}{suffix}{desc}")
        if node.is_directory and node.children:
            extension = "    " if i == len(nodes) - 1 else "│   "
            lines.extend(_render_file_tree(node.children, prefix + extension))
    return lines


def _detect_install_command(tech_stack: list[str]) -> str:
    """Infer the install command from the project's tech stack."""
    stack_lower = {t.lower() for t in tech_stack}
    if any(t in stack_lower for t in ("next.js", "nextjs", "react", "node", "typescript")):
        return "npm install"
    if any(t in stack_lower for t in ("poetry",)):
        return "poetry install"
    # Default Python
    return 'pip install -e ".[dev]"'


def _detect_run_command(tech_stack: list[str]) -> str:
    """Infer a sensible run command from the project's tech stack."""
    stack_lower = {t.lower() for t in tech_stack}
    if any(t in stack_lower for t in ("next.js", "nextjs", "react")):
        return "npm run dev"
    if any(t in stack_lower for t in ("fastapi", "uvicorn")):
        return "uvicorn app.main:app --reload"
    if any(t in stack_lower for t in ("flask",)):
        return "flask run"
    if any(t in stack_lower for t in ("django",)):
        return "python manage.py runserver"
    return "python -m app"


def _detect_test_command(tech_stack: list[str]) -> str:
    """Infer a test command from the project's tech stack."""
    stack_lower = {t.lower() for t in tech_stack}
    if any(t in stack_lower for t in ("next.js", "nextjs", "react", "node", "typescript")):
        return "npm test"
    return "pytest tests/ -v --cov=. --cov-report=term-missing"


